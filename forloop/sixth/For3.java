package com.onesoft.forloop.sixth;

public class For3 {




	 
	public static void main(String[] args) {
		int ca=Integer.parseInt(args[0]);
		int add=0;
		int total=0;
		for(int i=0;i<=ca;i++) {
			for(int j=0;j<=ca;i++) {
				add=add+i;
				total=add;}
				System.out.println(total);
			
			
		}

	}

	}



